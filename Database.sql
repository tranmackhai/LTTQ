CREATE FUNCTION [dbo].[FUNC_DOANHTHUTRONGNGAY]
(@NGAY DATETIME)
RETURNS DECIMAL
AS
BEGIN
	DECLARE @DOANHTHU_PHIM DECIMAL;
	DECLARE @DOANHTHU_DOAN DECIMAL;
	SELECT @DOANHTHU_PHIM = SUM(TONGTIEN)
	FROM VE
	WHERE
		DAY(NGAYLAP) = DAY(@NGAY) AND
		MONTH(NGAYLAP) = MONTH(@NGAY) AND
		YEAR(NGAYLAP) = YEAR(@NGAY)

	IF(@DOANHTHU_PHIM IS NULL)
		BEGIN
			SET @DOANHTHU_PHIM = 0
		END

	SELECT @DOANHTHU_DOAN = SUM(TONGTIEN)
	FROM HOADON
	WHERE
		DAY(NGAYHD) = DAY(@NGAY) AND
		MONTH(NGAYHD) = MONTH(@NGAY) AND
		YEAR(NGAYHD) = YEAR(@NGAY)

	IF(@DOANHTHU_DOAN IS NULL)
		BEGIN
			SET @DOANHTHU_DOAN = 0
		END

	RETURN @DOANHTHU_PHIM + @DOANHTHU_DOAN
END
GO
